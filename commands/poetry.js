function buildMenu() {
    let msg = `╔══════════════════════╗\n`;
    msg += `║    📜 *ديوان الشعر*    ║\n`;
    msg += `╚══════════════════════╝\n\n`;
    msg += `📚 *الأوامر:*\n`;
    msg += `• .بيت ← بيت عشوائي\n`;
    msg += `• .بيت [تصنيف] ← مثال: .بيت غزل\n`;
    msg += `• .بيت [شاعر] ← مثال: .بيت المتنبي\n`;
    msg += `• .بيت [شاعر] [تصنيف] ← مثال: .بيت المتنبي حكمة\n`;
    msg += `• .قصيدة ← قصيدة عشوائية\n`;
    msg += `• .قصيدة [شاعر] ← مثال: .قصيدة شوقي\n\n`;
    msg += `🎭 *التصنيفات:*\n`;
    msg += categories.join(' • ') + '\n\n';
    msg += `✍️ *الشعراء:*\n`;
    msg += poets.join(' • ');
    return msg;
}

function getRandom(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

function findPoet(query) {
    return poets.find(p => p.includes(query) || query.includes(p.split(' ').pop()));
}

function findCategory(query) {
    return categories.find(c => c === query || query.includes(c));
}

function formatPoem(poem) {
    return `📜 *${poem.poet}*\n🎭 _${poem.category}_\n\n${poem.text}`;
}

